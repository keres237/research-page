    <h2>Extension Service Philosophy</h2>
    <ul>
        <p>WMSU Extension Services is guided by the philosophy that effectiveness of  services is measured by the improvement of the quality of life of the beneficiary  communities, as it: </p>
            <p>1. Considers "Adopt a community" as an innovative strategy for change and  development; </p>
            <p>2. Implement programs to address the basic needs of the beneficiary  communities;</p>
            <p>3. Provides services for sustainable socio-economic development; </p>
            <p>4. Forges with other agencies for stronger partnership, creating greater impact  on the lives of the people and community it serves.</p>
